"""整数类型输出格式"""
#b:输出整数的二进制
#c:输出整数的Unicode字符
#d:输出整数的十进制
#o:输出整数的八进制
#x:输出整数的小写十六进制
#X:输出整数的大写十六进制
""" 
o='{0:b},{0:c},{0:d},{0:o},{0:x},{0:X}'.format(120)
print(o) 
"""
#结果：1111000,x,120,170,78,78
""" 
o='{0:b},{0:c},{0:d},{0:o},{0:x},{0:X}'.format(425)
print(o) 
""" 
#结果：110101001,Ʃ,425,651,1a9,1A9

"""浮点数类型输出格式"""
#e:输出浮点数对应的小写字母e的指数形式
#E:输出浮点数对应的大写字母E的指数形式
#f:输出浮点数对应的标准浮点形式
#%:输出浮点数对应的百分比形式
""" 
p='{0:e},{0:E},{0:f},{0:%}'.format(3.14)
print(p) 
"""
#结果：3.140000e+00,3.140000E+00,3.140000,314.000000%
'''
2位精度:
'''
p='{0:.2e},{0:.2E},{0:.2f},{0:.2%}'.format(3.14)
print(p)
#结果：3.14e+00,3.14E+00,3.14,314.00%