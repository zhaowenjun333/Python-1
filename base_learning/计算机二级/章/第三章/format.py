#语法格式：
#        模板字符串.format(逗号分隔的参数)
#        槽位用{}表示
"""
a='{}曰：学而时习之，不亦说乎'.format('孔子')
print(a)
b='{}曰：学而时习之，不亦{}'.format('孔子','说乎')
print(b) 
"""

#参数排序
#参数1号位对应{0},二号位对应{1}
""" 
c='{0}曰:学而时习之,不亦{1}'.format('孔子','说乎')
print(c)
d='{1}曰:学而时习之,不亦{0}'.format('说乎','孔子')
print(d)
"""

""" 
e='{0}开{0}落'.format('花')
print(e) 
"""

#format方法的格式控制
#语法格式：
#        {参数序号:格式控制标记}

#设置宽度，0是序号，25是指定宽度
#实际宽度4<指定宽度25，用指定宽度
#默认左对齐方式
""" e='{0:25}'.format('等级考试')
print(e) """

#<左对齐
#>右对齐
#^居中对齐
""" g='{0:^25}'.format('等级考试')
print(g) """

""" g='{0:>25}'.format('等级考试')
print(g) """

#填充空格处
""" h='{0:/^25}'.format('等级考试')
print(h) """

#指定宽度小于实际宽度,按照实际宽度
""" 
i='{0:*^1}'.format('等级考试')
print(i) 
"""


"""用于对数值本身的规范：，、.精度、类型。"""
'''①逗号(,)：用于显示数字类型的千位分隔符。'''
""" 
j='{:-^25,}'.format(1234567890)
print(j) 
"""
#结果：------1,234,567,890------

'''
②.精度：有小数点(.)开头，对于浮点数，精度表示小
数部分输出的有效位数，对于字符串，精度表示输出的
最大长度。如果小数点保留长度超过应输出的长度，以
应输出长度为准。
'''
""" 
k='{0:.2f}'.format(123.456789)
print(k) 
"""
#结果：123.46

""" 
z='{0:->25.3f}'.format(123.456789)
print(z)
#结果：------------------123.457
l='{0:->25,.3f}'.format(123456.789)
print(l)
#结果--------------123,456.789
"""

#字符串型
""" m='{0:.3}'.format('计算机等级考试')
print(m) """
#结果：计算机
""" 
n='{0:.25}'.format('计算机等级考试')
print(n) 
"""
#结果：计算机等级考试

