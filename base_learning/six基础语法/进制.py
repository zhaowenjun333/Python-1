# 二进制 0b
# 例子
0b10  # 十进制表示2

# 八进制 0o
# 例子
0o10  # 十进制表示8

# 十六进制 0x
# 例子
0x10  # 十进制表示16
0x1F  # 十进制表示31

#------------------#
# 进制转换

# 十进制转二进制
bin(10) # 0b1010

# 八进制转二进制
bin(0o7) # 0b111

# 十六进制转二进制
bin(0xE) # 0b1110

#--------------#
# 二进制转十进制
int(0b111) # 7

# 八进制转十进制
int(0o77) # 63

#--------------#
# 十进制转十六进制
hex(888) # 0x378

# 八进制转十六进制
hex(0o7777) # 0xfff

#--------------#
# 二进制转八进制
oct(0b111) # 0o7

#十六进制转八进制
oct(0x777) # 0o3567